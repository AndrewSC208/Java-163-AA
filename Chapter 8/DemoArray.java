public class DemoArray {
	public static void main(String[] args) {
		// Array that can hold four double values:
		double[] salary = new double[4];
		// Assign four values to the four salary array elements:
		salary[0] = 6.25;
		salary[1] = 6.55;
		salary[2] = 10.25;
		salary[3] = 16.85;

		// Print them out:
		System.out.println("Salaries one by one are:");
		System.out.println(salary[0]);
		System.out.println(salary[1]);
		System.out.println(salary[2]);
		System.out.println(salary[3]);
	}
}
